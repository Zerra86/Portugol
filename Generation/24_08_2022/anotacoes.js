/*

Aplicação: Permite o acesso aos recursos de rede. Interage com aplicações do software. Por exemplo, ao iniciar a transferência de arquivos ou enviar e-mails.

Transporte: Emite mensagens confiáveis e entrega de erros. Estabelece a quantidade de dados que deve ser transmitida a um local e a velocidade de envio. 
Utiliza Handshakes para veicular uma transmissão bem-sucedida.

Internet: Movimenta pacotes de A para B. Pode utilizar várias redes e nós para traçar.

Acesso á Rede: Responsável pela transmissão física entre dois dispositivos na mesma rede.

*/

