/*

pare/break: Faz o fluxo sair do bloco do código onde a palavra foi inserida.

caso/case: Executa um bloco de código dependendo da instrução switch.

continue: Faz com que o código existente após essa instrução não seja executado em um loop, indicando a próxima interação do loop.

caso contrario/default: Executará esse bloco de código se nenhuma das instruções switch/case for atendida.

faça/do: Executa um bloco de código um vez e, em seguida junto com a instrução while, processa um teste para determinar se o bloco deve ser executado novamente.

senao/else: Executa um bloco de código alternativo se uma condição se for falsa.

para/for: Usada para executar um loop condicional em um bloco de código

se/if: Usada para executar um teste lógico verdadeiro ou falso.

return: Faz o fluxo sair de um método sem executar nenhum código que seja após a instrução (pode retornar uma variável opcionalmente);

escolha/switch: Indica a variável a ser confrontada com as instruções caso.

enquanto/while: Executa um bloco de código repetidamente, enquanto um certa condição for verdadeira.


Logico: Não é um valor numérico, só admite os valores true ou false.

Caracter: letras/palavras. Usa o código UNICODE e ocupa cada caractere 16 bits.

Inteiros: Números inteiros, podem ser positivos ou negativos.

Reais em ponto flutuante: números com casas decimais.

*/
































